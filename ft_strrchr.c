#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	int		len;

	len = ft_strlen((char *)s);
	i = len - 1;
	if (c == '\0')
		return ((char *)s + len);
	while (i >= 0)
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		i--;
	}
	return (0);
}
